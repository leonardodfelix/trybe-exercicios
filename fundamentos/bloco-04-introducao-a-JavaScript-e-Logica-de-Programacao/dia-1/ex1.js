let num1 = 10;
let num2 = 5;

function addNumbers(x1, x2){
  return console.log(x1 + x2);
}

function subtNumbers(x1, x2) {
  return console.log(x1 - x2);  
}

function multNumbers(x1, x2) {
  return console.log(x1 * x2);  
}

function divNumbers(x1, x2) {
  return console.log(x1 / x2)
}

function modNumbers(x1, x2){
  return console.log(x1 % x2)
}

addNumbers(num1, num2);
subtNumbers(num1, num2);
multNumbers(num1, num2);
divNumbers(num1, num2);
modNumbers(num1, num2);